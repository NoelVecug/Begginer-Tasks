package Task_9;

public class Swap {
    public static void main(String[] args) {
        // Write a Java program to swap two variables.
        int num1 = 10;
        int num2 = 21;

        int var = num1;
        num1 = num2;
        num2 = var;
        System.out.println("First number: " + num1);
        System.out.println("Second number: " + num2);
    }
}
